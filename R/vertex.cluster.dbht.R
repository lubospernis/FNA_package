vertex.cluster.dbht <- function(cliques,id='(all networks)',separating_cliques='(empty)',bubble_save_id='bubble',save_as='dbht',weight_property='value',save_to='None',debug='false'){ 
 l <- as.list(match.call()) 
 l2 <- list() 
 for (i in names(l[-1])) { 
 l2 <- c(l2, eval(dplyr::sym(i))) 
 } 
 names(l2) <- names(l[-1]) 
 l3 <- c(l[1], l2) 
 FNA::exec_command(FNA::check(l3))
 }