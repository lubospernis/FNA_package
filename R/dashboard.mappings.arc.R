dashboard.mappings.arc <- function(width=':1-8::1',color=':black',style='(empty)',opacity=':0.2-0.7::0.7',arrow=':1',validate='true',save_to='None',debug='false'){ 
 l <- as.list(match.call()) 
 l2 <- list() 
 for (i in names(l[-1])) { 
 l2 <- c(l2, eval(dplyr::sym(i))) 
 } 
 names(l2) <- names(l[-1]) 
 l3 <- c(l[1], l2) 
 FNA::exec_command(FNA::check(l3))
 }